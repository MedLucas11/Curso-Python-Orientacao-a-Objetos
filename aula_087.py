# O que é orientação a objetos?

# Orientação a objetos é um paradigma de programação
# Um paradigma é um conjunto de crenças, valores e técnicas compartilhadas por membros de uma determinada comunidade
# Portanto, um paradigma de programação é um conjunto de regras (forma de pensar) utilizadas para resolver problemas.
# É um estilo de programação, uma metodologia utilizada para resolver um problema de forma computacional


# No paradigma orientado a objetos, o foco está nos elementos componentes da solução (objetos)
# Cada elemento possui dados e comportamentos

# Além dos conceitos trabalhados até agora de programação estruturada, a orientação a objetos trata de conceitos como
# encapsulamento, herança e polimorfismo

# Os objetos possuem comportamentos (métodos) e características (atributos). Eles são encapsulados e o processo de
# comunicação entre objetos e o mundo real acontece por troca de mensagens.

