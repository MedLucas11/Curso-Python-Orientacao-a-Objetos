# Pilares da orientação a objetos

# Encapsulamento
# Propriedade de você utilizar um objeto sem entender seu funcionamento interno
# Encapsular todos os comportamentos e as informações de cada um dos objetos


# Herança
# Propriedade de uma classe filha (subclasse) herdar componentes da classe pai (superclasse/classe superior)


# Polimorfismo
# Propriedade que permite que uma mesma mensagem seja enviada a diferentes objetos e que cada objeto execute
# a operação apropriada a sua classe (sobreposição de métodos)


# Abstração
# Propriedade de esconder os detalhes da implementação dentro de algo. Forma utilizada para lidar com a complexidade
# do mundo
# Por exemplo: quando chamamos uma função não precisamos saber como ela realizará o processamento interno. Nos interessa
# apenas o resultado.


